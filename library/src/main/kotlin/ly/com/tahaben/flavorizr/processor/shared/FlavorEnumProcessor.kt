package ly.com.tahaben.flavorizr.processor.shared

import ly.com.tahaben.flavorizr.model.FlavorConfig
import ly.com.tahaben.flavorizr.processor.AbstractProcessor
import java.io.File

/**
 * Generates a Kotlin Flavor enum into the consumer's source set.
 * The enum contains all configured flavors with their display names.
 */
class FlavorEnumProcessor : AbstractProcessor() {
    override val name = "shared:flavorEnum"

    override fun execute(config: FlavorConfig, projectDir: File) {
        val content = generateFlavorEnum(config)
        val packageDir = config.flavorEnumPackage.replace('.', '/')
        val outputDir = File(projectDir, "build/generated/kmpFlavorizr/commonMain/kotlin/$packageDir")
        outputDir.mkdirs()
        val outputFile = File(outputDir, "Flavor.kt")
        outputFile.writeText(content)
        log("Generated: Flavor.kt (package: ${config.flavorEnumPackage})")
    }

    companion object {
        fun generateFlavorEnum(config: FlavorConfig, currentFlavorName: String? = null): String {
            val flavorKeys = config.flavors.keys
            val matchedFlavor = currentFlavorName
                ?.let { name -> flavorKeys.firstOrNull { it.equals(name, ignoreCase = true) } }

            return buildString {
                appendLine("package ${config.flavorEnumPackage}")
                appendLine()
                appendLine("/**")
                appendLine(" * Auto-generated by kmp-flavorizr. Do not edit manually.")
                appendLine(" */")
                appendLine("enum class Flavor(")
                appendLine("    val appName: String,")
                appendLine(") {")

                val flavors = config.flavors.entries.toList()
                for ((index, entry) in flavors.withIndex()) {
                    val (flavorName, flavor) = entry
                    val enumName = flavorName.uppercase()
                    val separator = if (index < flavors.size - 1) "," else ";"

                    appendLine("    $enumName(appName = \"${flavor.app.name}\")$separator")
                }

                appendLine()
                appendLine("    companion object {")
                val currentLiteral = matchedFlavor?.uppercase() ?: "null"
                appendLine("        /**")
                appendLine("         * The flavor the app was built with, or `null` if it could not be determined.")
                appendLine("         *")
                appendLine("         * On Android this is resolved from the Gradle task name (e.g. `assembleProdRelease`)")
                appendLine("         * at build time, so it is reliable for any build produced by Gradle.")
                appendLine("         *")
                appendLine("         * On iOS this is only populated when the app is built and launched from Xcode,")
                appendLine("         * because the active flavor is propagated through the Xcode scheme's environment.")
                appendLine("         * If the iOS app is launched from IntelliJ IDEA or Android Studio (or any other")
                appendLine("         * runner that does not invoke the Xcode scheme), this will be `null` even though")
                appendLine("         * a flavor is configured. In that case, fall back to a sensible default in your")
                appendLine("         * code rather than treating `null` as an error.")
                appendLine("         */")
                appendLine("        val current: Flavor? = $currentLiteral")
                appendLine("        fun fromName(name: String): Flavor =")
                appendLine("            entries.first { it.name.equals(name, ignoreCase = true) }")
                appendLine("    }")
                appendLine("}")
                appendLine()
            }
        }
    }
}
