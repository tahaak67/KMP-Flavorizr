package ly.com.tahaben.flavorizr.processor.android

import ly.com.tahaben.flavorizr.model.*
import ly.com.tahaben.flavorizr.processor.AbstractProcessor
import java.io.File

/**
 * Generates flavorizr.gradle (Groovy) containing all productFlavors configuration.
 * Apply via `apply from: 'flavorizr.gradle'` in the consumer's build.gradle,
 * or `apply(from = "flavorizr.gradle")` in build.gradle.kts.
 */
class AndroidFlavorizrGradleProcessor : AbstractProcessor() {
    override val name = "android:flavorizrGradle"

    override fun execute(config: FlavorConfig, projectDir: File) {
        File(projectDir, "flavorizr.gradle.kts").delete()
        val outputFile = File(projectDir, "flavorizr.gradle")
        val content = generateGroovyDsl(config)
        outputFile.writeText(content)
        log("Generated: flavorizr.gradle")
    }

    private fun generateGroovyDsl(config: FlavorConfig): String {
        val flavorDimensions = config.globalConfig.android?.flavorDimensions ?: "flavor-type"
        val globalResValues = config.globalConfig.android?.resValues ?: emptyMap()
        val globalBuildConfigFields = config.globalConfig.android?.buildConfigFields ?: emptyMap()
        val hasBuildConfig = globalBuildConfigFields.isNotEmpty() ||
                config.androidFlavors.any { it.value.android!!.buildConfigFields.isNotEmpty() }

        return buildString {
            appendLine("// Auto-generated by kmp-flavorizr. Do not edit manually.")
            appendLine("// Apply this file in your build.gradle with: apply from: 'flavorizr.gradle'")
            appendLine()
            appendLine("android {")
            appendLine("    flavorDimensions '$flavorDimensions'")
            appendLine()
            appendLine("    productFlavors {")

            for ((flavorName, flavor) in config.androidFlavors) {
                val android = flavor.android!!
                appendLine("        create('$flavorName') {")
                appendLine("            dimension = '$flavorDimensions'")
                appendLine("            applicationId = '${android.applicationId}'")

                for ((key, value) in android.customConfig) {
                    appendLine("            $key = ${formatCustomConfigValue(value)}")
                }

                val mergedResValues = buildMergedResValues(flavor, globalResValues)
                for ((resName, resValue) in mergedResValues) {
                    val escapedValue = resValue.value.replace("'", "\\'")
                    appendLine("            resValue '${resValue.type}', '$resName', '$escapedValue'")
                }

                val mergedBcf = buildMergedBuildConfigFields(android, globalBuildConfigFields)
                for ((fieldName, field) in mergedBcf) {
                    val wrappedValue = wrapBuildConfigValue(field.type, field.value)
                    appendLine("            buildConfigField '${field.type}', '$fieldName', $wrappedValue")
                }

                appendLine("        }")
            }

            appendLine("    }")

            if (hasBuildConfig) {
                appendLine()
                appendLine("    buildFeatures {")
                appendLine("        buildConfig = true")
                appendLine("    }")
            }

            appendLine("}")
            appendLine()
        }
    }

    private fun buildMergedResValues(
        flavor: Flavor,
        globalResValues: Map<String, ResValue>,
    ): Map<String, ResValue> {
        val merged = linkedMapOf<String, ResValue>()
        merged["app_name"] = ResValue("string", flavor.app.name)
        merged.putAll(globalResValues)
        merged.putAll(flavor.android!!.resValues)
        return merged
    }

    private fun buildMergedBuildConfigFields(
        android: AndroidFlavor,
        globalFields: Map<String, BuildConfigField>,
    ): Map<String, BuildConfigField> {
        val merged = linkedMapOf<String, BuildConfigField>()
        merged.putAll(globalFields)
        merged.putAll(android.buildConfigFields)
        return merged
    }

    private fun formatCustomConfigValue(value: Any): String {
        return when (value) {
            is String -> "'$value'"
            is Int, is Long, is Boolean -> value.toString()
            else -> value.toString()
        }
    }

    private fun wrapBuildConfigValue(type: String, value: String): String {
        return when (type) {
            "String" -> "'\"$value\"'"
            "char" -> "\"'$value'\""
            else -> "'$value'"
        }
    }
}
