package ly.com.tahaben.flavorizr.processor.ios

import ly.com.tahaben.flavorizr.model.FlavorConfig
import ly.com.tahaben.flavorizr.processor.AbstractProcessor
import java.io.File

/**
 * Injects a project declaration into the Podfile that maps each flavor configuration
 * to its CocoaPods build type (debug/release).
 */
class IosPodfileProcessor(
    private val iosProjectPath: String,
    private val iosTargetName: String,
) : AbstractProcessor() {
    override val name = "ios:podfile"

    companion object {
        private const val SECTION = "flavorConfigurations"
    }

    override fun execute(config: FlavorConfig, projectDir: File) {
        val podfile = File(projectDir, "$iosProjectPath/Podfile")
        if (!podfile.exists()) {
            log("Podfile not found, skipping")
            return
        }

        val projectDeclaration = generateProjectDeclaration(config)
        var content = podfile.readText()

        // Use # markers for Ruby files
        val begin = "# ----- BEGIN $SECTION (autogenerated by kmp-flavorizr) -----"
        val end = "# ----- END $SECTION (autogenerated by kmp-flavorizr) -----"

        if (content.contains(begin) && content.contains(end)) {
            // Replace existing section
            val pattern = Regex(
                Regex.escape(begin) + ".*?" + Regex.escape(end),
                RegexOption.DOT_MATCHES_ALL,
            )
            content = content.replace(pattern, "$begin\n$projectDeclaration\n$end")
        } else {
            // Insert at the beginning of the file (before target declarations)
            val targetPattern = Regex("""^target\s+""", RegexOption.MULTILINE)
            val match = targetPattern.find(content)

            if (match != null) {
                val insertPoint = match.range.first
                content = buildString {
                    append(content.substring(0, insertPoint))
                    appendLine(begin)
                    appendLine(projectDeclaration)
                    appendLine(end)
                    appendLine()
                    append(content.substring(insertPoint))
                }
            } else {
                // Append at end
                content += "\n$begin\n$projectDeclaration\n$end\n"
            }
        }

        podfile.writeText(content)
        log("Modified: Podfile")
    }

    private fun generateProjectDeclaration(config: FlavorConfig): String {
        // Find the xcodeproj name in the iOS directory
        val iosDir = File(config.iosProjectPath)
        val xcodeprojName = iosTargetName

        return buildString {
            appendLine("project '$xcodeprojName', {")
            for ((flavorName, _) in config.iosFlavors) {
                appendLine("  'Debug-$flavorName' => :debug,")
                appendLine("  'Profile-$flavorName' => :release,")
                appendLine("  'Release-$flavorName' => :release,")
            }
            append("}")
        }
    }
}
