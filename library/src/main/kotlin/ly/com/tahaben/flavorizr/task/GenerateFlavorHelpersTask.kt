package ly.com.tahaben.flavorizr.task

import org.gradle.api.DefaultTask
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.provider.ListProperty
import org.gradle.api.tasks.CacheableTask
import org.gradle.api.tasks.Input
import org.gradle.api.tasks.OutputFile
import org.gradle.api.tasks.TaskAction

@CacheableTask
abstract class GenerateFlavorHelpersTask : DefaultTask() {

    @get:Input
    abstract val flavorNames: ListProperty<String>

    @get:OutputFile
    abstract val outputFile: RegularFileProperty

    init {
        group = "kmp-flavorizr"
        description = "Generates flavorizrHelpers.gradle.kts with flavor detection utilities"
    }

    @TaskAction
    fun generate() {
        val names = flavorNames.get()
        val file = outputFile.get().asFile
        file.parentFile?.mkdirs()
        file.writeText(buildContent(names))
        logger.lifecycle("[kmp-flavorizr] Generated: ${file.absolutePath}")
    }

    companion object {
        fun buildContent(flavorNames: List<String>): String {
            val flavorSet = flavorNames.joinToString(", ") { "\"$it\"" }
            return buildString {
                appendLine("// Auto-generated by kmp-flavorizr. Do not edit manually.")
                appendLine("// Regenerate with: ./gradlew generateFlavorHelpers")
                appendLine("//")
                appendLine("// Option 1 — preferred: call extension functions provided by the plugin directly in your build.gradle.kts")
                appendLine("//   val flavor: String? = getAndroidBuildFlavorOrNull()")
                appendLine("//   val flavor: String? = currentBuildFlavorOrNull()")
                appendLine("//")
                appendLine("// Option 2 — apply this file then read extra properties:")
                appendLine("//   apply(from = \"\${rootProject.projectDir}/build/kmpFlavorizr/flavorizrHelpers.gradle.kts\")")
                appendLine("//   val flavor = extra[\"currentBuildFlavor\"] as String?")
                appendLine()
                appendLine("import java.util.regex.Pattern")
                appendLine()
                appendLine("val kmpFlavorNames: Set<String> = setOf($flavorSet)")
                appendLine()
                appendLine("fun getAndroidBuildFlavorOrNull(): String? {")
                appendLine("    val taskRequestsStr = gradle.startParameter.taskRequests.toString()")
                appendLine("    val pattern: Pattern = if (taskRequestsStr.contains(\"assemble\")) {")
                appendLine("        Pattern.compile(\"assemble(\\\\w+)(Release|Debug)\")")
                appendLine("    } else {")
                appendLine("        Pattern.compile(\"bundle(\\\\w+)(Release|Debug)\")")
                appendLine("    }")
                appendLine("    val matcher = pattern.matcher(taskRequestsStr)")
                appendLine("    val flavor = if (matcher.find()) matcher.group(1).lowercase() else null")
                appendLine("    return if (flavor in kmpFlavorNames) flavor else null")
                appendLine("}")
                appendLine()
                appendLine("fun currentBuildFlavorOrNull(): String? {")
                appendLine("    return getAndroidBuildFlavorOrNull()")
                appendLine("        ?: System.getenv()[\"FLAVOR\"]?.takeIf { it in kmpFlavorNames }")
                appendLine("}")
                appendLine()
                appendLine("extra[\"kmpFlavorNames\"] = kmpFlavorNames")
                appendLine("extra[\"currentBuildFlavor\"] = currentBuildFlavorOrNull()")
                appendLine("extra[\"androidBuildFlavor\"] = getAndroidBuildFlavorOrNull()")
            }
        }
    }
}
